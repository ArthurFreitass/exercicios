// função normal

function soma(numero1, numero2) {
  return numero1 + numero2;
}

console.log(soma(100, 50));

// função arrow

const somaArrow = (num1, num2) => {
  return num1 + num2;
};

console.log(somaArrow(20, 30));

// simplificando a arrow function

const subtraçãoArrow = (num1, num2) => num1 - num2;

console.log(subtraçãoArrow(20, 10));
