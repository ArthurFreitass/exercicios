// Objeto
const objetoFrutas = {
  Melancia: 0,
  Abacate: 0,
  Uva: 0,
};

// Funções

function CountFruits(array) {
  if (typeof array === "object") {
    for (let index = 0; index < array.length; index += 1) {
      if (array[index] === "Melancia") {
        objetoFrutas.Melancia += 1;
      } else if (array[index] === "Abacate") {
        objetoFrutas.Abacate += 1;
      } else if (array[index] === "Uva") {
        objetoFrutas.Uva += 1;
      }
    }
  }
  return objetoFrutas;
}

